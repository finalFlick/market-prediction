export function getComponent(id: string): ComponentMeta | undefined {
  return components.find((c) => c.id === id);
}

export function componentsByCategory(): Record<ComponentCategory, ComponentMeta[]> {
  const out: Record<ComponentCategory, ComponentMeta[]> = {
    primitives: [],
    data: [],
    charts: [],
    operator: [],
    navigation: [],
  };
  for (const c of components) {
    out[c.category].push(c);
  }
  return out;
}

export const productionComponentCount = components.filter((c) => c.status === "production").length;
